    private void elegirMenuDinamico(String opcion){
        switch (opcion) {
            case "pnlGestionUbicaciones":
                // Desactivamos paneles
                pnlGestionLibros.setVisible(false);
                pnlGestionPersonas.setVisible(false);
                
                // Activamos panel correcto y repintamos
                pnlGestionUbicaciones.setVisible(true);
                pnlGestionUbicaciones.repaint();
                break;
            case "pnlGestionLibros":
                pnlGestionUbicaciones.setVisible(false);
                pnlGestionPersonas.setVisible(false);
                
                pnlGestionLibros.setVisible(true);
                pnlGestionLibros.repaint();
                break;
            case "pnlGestionPersonas":
                pnlGestionLibros.setVisible(false);
                pnlGestionUbicaciones.setVisible(false);
                
                pnlGestionPersonas.setVisible(true);
                pnlGestionPersonas.repaint();
                break;
            default:
                throw new AssertionError();
        }
    }
    
